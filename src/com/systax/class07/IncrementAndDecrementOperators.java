package com.systax.class07;

public class IncrementAndDecrementOperators {

	public static void main(String[] args) {
		int x=10;
		x=x+1;
		x+=1;
		System.out.println(x);
		x++;//inrements the value of the variable by 1
		System.out.println(x);
		x--;//decrement the value of the variable by 1
		System.out.println(x);
		//increment and decrement always work with a variable
		int a=100;
		a++;
		System.out.println(a);
		
				

	}

}
