package com.systax.class07;

public class DoWhileExample {

	public static void main(String[] args) {
		int a=1;
		do {
		System.out.print(a+" ");
		a++;
		}while(a<=30);
		System.out.println("-----");
		//print even numbers from 70 to 30
		int b=70;
		do {
			System.out.print(b+" ");
			b-=2;
		}while(b>=30);
		

	}

}
