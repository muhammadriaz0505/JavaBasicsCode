package com.syntax.class08;

public class ForLoopsExamples {

	public static void main(String[] args) {
		//I need to print numbers from 1 to 90
		for(int i=1; i<=90; i++) {
			System.out.print(i+" ");
		}
		// I need numbers from 60 to 10
		for(int a=60; a>=10; a--) {
			System.out.print(a+" ");
		}
		for(int i=5; i<=40; i+=5) {
			System.out.print(i+" ");
		}


	}

}
