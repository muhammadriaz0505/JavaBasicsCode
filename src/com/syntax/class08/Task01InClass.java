package com.syntax.class08;

public class Task01InClass {

	public static void main(String[] args) {
		//Print even numbers from 20 to 1
		for(int i=20; i>=1; i-=2 ) {
			System.out.print(i+" ");
		}
		System.out.println("----------");
		//Print even numbers from 1 to 20
		for(int a=2; a<=20; a+=2) {
			System.out.print(a+" ");
		}
		System.out.println("------");
		//print odd nubers between 20 to 50
		for(int b=21; b<=50; b+=2) {
			
			System.out.print(b+" ");
		}
		System.out.println("-----");
		for(int c=50; c>=20; c-=2) {
			System.out.print(c+" ");
		}
		

	}

}
