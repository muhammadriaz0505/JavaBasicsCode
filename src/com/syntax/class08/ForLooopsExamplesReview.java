package com.syntax.class08;

public class ForLooopsExamplesReview {

	public static void main(String[] args) {
		// I need to print numbers from 1 to 90
		for(int i=1; i<=90; i++) {
			System.out.print(i+" ");
		}
		//I need 60 to 10
		System.out.println("-----------------------------");
		for(int i=60; i>=10; i--) {
			System.out.print(i+" ");
		}
		System.out.println();
		//what is the output?
		for(int i=5; i<=40; i+=5) {
			System.out.print(i+" ");
		}
	}

}
